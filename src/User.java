
public class User {
private String name=" ";
private int score=0;
public User() {}
public User(String name) {
	this.name=name;
}
public void setName(String name) {
	this.name=name;
}
public String getName() {
	return name;
}
public void setScore(int score) {
	this.score=score;
}
public int getScore() {
	return score;
}
}
